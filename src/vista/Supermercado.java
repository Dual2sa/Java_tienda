    @Override
    public void run() {
        int incremento = 30;
        while (true) {
            this.lblMov.setBounds(x, -5, 160, 25);

            if (x < 20) {
                this.lblMov.setForeground(Color.MAGENTA);
                incremento = 10;
            }
            if (x > 220) {
                this.lblMov.setForeground(Color.red);
                incremento = -10;
            }
            try {
                hilo.sleep(200);
            } catch (InterruptedException ex) {
                Logger.getLogger(Supermercado.class.getName()).log(Level.SEVERE, null, ex);
            }
            x = x + incremento;
        }
    }

